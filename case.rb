fav_color = 'pink'

case fav_color
when 'red'
	puts "Red like fire!"
when 'orange'
	puts "Orange like, well... an orange."
when 'yellow'
	puts "Yellow daffodils are so pretty in the spring!"
when 'green'
	puts "Have you been to the Emerald City in Oz?"
when 'blue'
	puts "Blue like the sky!"
when 'purple'
	puts "Purple plums are the tastiest."
else
	puts "Hmm, well I don't know what that color is!"
end

weather ='sunny'

case weather
when 'rainy' 
	puts "Wear those cute rainboots!"
when 'snowy'
	puts "Bundle up in scarves and boots!"
when 'cloudy'
	puts "Wear that cute sweater and bring an umbrella just in case!"
when 'foggy'
	puts "Wear something bright to cheer up on this gloomy day!"
when 'windy'
	puts "Best plan to wear some pants, don't want to pull a Marilyn!"
when 'sunny'
	puts "Bust out those sunnies and flip flops and enjoy!"
else 
	puts "Well this is tricky, dress for it all!"
end